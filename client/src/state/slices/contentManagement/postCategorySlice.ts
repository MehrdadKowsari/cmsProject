import { createAsyncThunk, createSlice } from '@reduxjs/toolkit'
import { AxiosError } from 'axios';
import axios from 'src/api/axios';
import { AddPostCategoryDTO } from 'src/models/contentManagement/postCategory/addPostCategoryDTO';
import { UpdatePostCategoryDTO } from 'src/models/contentManagement/postCategory/updatePostCategoryDTO';
import { PostCategoryDTO } from 'src/models/contentManagement/postCategory/postCategoryDTO';
import { IntialState } from 'src/state/interfaces/intialState';
import { GridParameter } from 'src/models/shared/grid/gridPrameter';

const API_URL: string = `${process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000'}/postCategory`;

export const add = createAsyncThunk(
  "postCategories/add", 
  async (postCategory: AddPostCategoryDTO, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/add`, postCategory)
      return data?.result;
    } catch (err) {
        const error= err as AxiosError;
        return rejectWithValue(error.message);
    }
});

export const getAllPostCategories = createAsyncThunk(
  "postCategories/getAll", 
  async (_, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/getAll`);
      return data?.result;
    } catch (err) {
      const error= err as AxiosError;
      return rejectWithValue(error.message);
    }
});

