import { createAsyncThunk, createSlice } from '@reduxjs/toolkit'
import { AxiosError } from 'axios';
import axios from 'src/api/axios';
import { AddContentBlockDTO } from 'src/models/contentManagement/contentBlock/addContentBlockDTO';
import { UpdateContentBlockDTO } from 'src/models/contentManagement/contentBlock/updateContentBlockDTO';
import { ContentBlockDTO } from 'src/models/contentManagement/contentBlock/contentBlockDTO';
import { IntialState } from 'src/state/interfaces/intialState';
import { GridParameter } from 'src/models/shared/grid/gridPrameter';

const API_URL: string = `${process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000'}/contentBlock`;

export const add = createAsyncThunk(
  "contentBlocks/add", 
  async (contentBlock: AddContentBlockDTO, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/add`, contentBlock)
      return data?.result;
    } catch (err) {
        const error= err as AxiosError;
        return rejectWithValue(error.message);
    }
});

