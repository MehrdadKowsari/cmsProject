import { createAsyncThunk, createSlice } from '@reduxjs/toolkit'
import { AxiosError } from 'axios';
import axios from 'src/api/axios';
import { AddSliderDTO } from 'src/models/contentManagement/slider/addSliderDTO';
import { UpdateSliderDTO } from 'src/models/contentManagement/slider/updateSliderDTO';
import { SliderDTO } from 'src/models/contentManagement/slider/sliderDTO';
import { IntialState } from 'src/state/interfaces/intialState';
import { GridParameter } from 'src/models/shared/grid/gridPrameter';

const API_URL: string = `${process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000'}/slider`;

export const add = createAsyncThunk(
  "sliders/add", 
  async (slider: AddSliderDTO, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/add`, slider)
      return data?.result;
    } catch (err) {
        const error= err as AxiosError;
        return rejectWithValue(error.message);
    }
});

export const getAllSliders = createAsyncThunk(
  "sliders/getAll", 
  async (_, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/getAll`);
      return data?.result;
    } catch (err) {
      const error= err as AxiosError;
      return rejectWithValue(error.message);
    }
});

