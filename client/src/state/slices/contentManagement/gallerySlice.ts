import { createAsyncThunk, createSlice } from '@reduxjs/toolkit'
import { AxiosError } from 'axios';
import axios from 'src/api/axios';
import { AddGalleryDTO } from 'src/models/contentManagement/gallery/addGalleryDTO';
import { UpdateGalleryDTO } from 'src/models/contentManagement/gallery/updateGalleryDTO';
import { GalleryDTO } from 'src/models/contentManagement/gallery/galleryDTO';
import { IntialState } from 'src/state/interfaces/intialState';
import { GridParameter } from 'src/models/shared/grid/gridPrameter';

const API_URL: string = `${process.env.NEXT_PUBLIC_API_URL || 'http://localhost:5000'}/gallery`;

export const add = createAsyncThunk(
  "galleries/add", 
  async (gallery: AddGalleryDTO, { rejectWithValue }) => {
    try {
      const { data } = await axios.post(`${API_URL}/add`, gallery)
      return data?.result;
    } catch (err) {
        const error= err as AxiosError;
        return rejectWithValue(error.message);
    }
});

